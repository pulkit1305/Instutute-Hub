    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:


         try (Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/pulkit", "springstudent", "springstudent")) {
            String sql = "UPDATE users SET name = ?, age = ?, contact = ?, email = ?, password = ? WHERE username = ?";
            PreparedStatement stmt = con.prepareStatement(sql);

            stmt.setString(1, nameField.getText());
            stmt.setString(2, ageField.getText());
            stmt.setString(3, contactField.getText());
            stmt.setString(4, emailField.getText());
            stmt.setString(5, passwordField.getText());
            stmt.setString(6, enteredUsername);

            int rowsUpdated = stmt.executeUpdate();
            if (rowsUpdated > 0) {
                JOptionPane.showMessageDialog(this, "Profile updated successfully!");
            } else {
                JOptionPane.showMessageDialog(this, "Update failed. Please try again.");
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error updating profile: " + e.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

