    private void loadUserDetails() {
        try (Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/pulkit", "springstudent", "springstudent")) {
            String sql = "SELECT * FROM users WHERE username = ?";
            PreparedStatement stmt = con.prepareStatement(sql);
            stmt.setString(1, enteredUsername);
            ResultSet rs = stmt.executeQuery();

            if (rs.next()) {
                nameField.setText(rs.getString("name"));
                ageField.setText(rs.getString("age"));
                contactField.setText(rs.getString("contact"));
                emailField.setText(rs.getString("email"));
                usernameField.setText(rs.getString("username"));
                passwordField.setText(rs.getString("password"));
                usernameField.setEditable(false); // Username is non-editable
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error loading user details: " + e.getMessage());
        }
    }





