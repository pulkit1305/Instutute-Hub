    public void insertdata() {

        try {
            // Connect to database
            Class.forName("com.mysql.cj.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/pulkit?zeroDateTimeBehavior=CONVERT_TO_NULL", "springstudent", "springstudent");
            if (con != null) {
                JOptionPane.showMessageDialog(null, "Server is connected", "Connection MSG", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (Exception e) {
            System.out.println(e);
        }


        // Construct the SQL UPDATE query to update the existing record
        String updateQuery = "";
        if (admin.isSelected()) {
           
              try {
                s1 = "INSERT INTO admin(username, password, email) VALUES('" + username + "', '" + password + "', '" + email + "')";

               
                stm = con.createStatement();
                i = stm.executeUpdate(s1);
                if (i > 0) {
             
                    JOptionPane.showMessageDialog(null, "Admin Registered sucessfull", "Insert MSG", JOptionPane.INFORMATION_MESSAGE);
                } else {

                    JOptionPane.showMessageDialog(null, "Error in inserting data", "Insert MSG", JOptionPane.INFORMATION_MESSAGE);
                }

            } catch (Exception e) {
                System.out.println(e);
            }
        } else {
            try {
                // Update user record with matching email (if email matches, update the username and password)
                updateQuery = "UPDATE users SET username = ?, password = ? WHERE email = ?";

                PreparedStatement stmt = con.prepareStatement(updateQuery);
                stmt.setString(1, username);
                stmt.setString(2, password);
                stmt.setString(3, email);

                int i = stmt.executeUpdate();
                if (i > 0) {
                    JOptionPane.showMessageDialog(null, "User Registered successfully", "Insert MSG", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(null, "Error in updating data", "Insert MSG", JOptionPane.INFORMATION_MESSAGE);
                }
            } catch (Exception e) {
                System.out.println(e);
            }

        }
    }


