    private void populateCompanies() {
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection centralCon = DriverManager.getConnection(
                    "jdbc:mysql://localhost:3306/central_db?zeroDateTimeBehavior=CONVERT_TO_NULL", "springstudent",
                    "springstudent");

            String query = "SELECT name FROM companies";
            PreparedStatement ps = centralCon.prepareStatement(query);
            ResultSet rs = ps.executeQuery();

            while (rs.next()) {
                companyComboBox.addItem(rs.getString("name"));
            }
            centralCon.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Failed to load companies: " + e.getMessage(), "Error",
                    JOptionPane.ERROR_MESSAGE);
        }
    }

    public void validating() {
        try {
            // Get the selected company
            selectedCompany = (String) companyComboBox.getSelectedItem();

            // Connect to central_db to fetch the database name for the selected company
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection centralCon = DriverManager.getConnection(
                    "jdbc:mysql://localhost:3306/central_db?zeroDateTimeBehavior=CONVERT_TO_NULL", "springstudent",
                    "springstudent");
            String dbQuery = "SELECT database_name FROM companies WHERE name=?";
            PreparedStatement dbPs = centralCon.prepareStatement(dbQuery);
            dbPs.setString(1, selectedCompany);
            ResultSet dbRs = dbPs.executeQuery();

            if (dbRs.next()) {
                String databaseName = dbRs.getString("database_name");

                // Connect to the company's database
                con = DriverManager.getConnection("jdbc:mysql://localhost:3306/" + databaseName, "springstudent",
                        "springstudent");

                if (admin.isSelected()) {
                    String adminQuery = "SELECT * FROM admin WHERE username=? AND password=?";
                    PreparedStatement adminPs = con.prepareStatement(adminQuery);
                    adminPs.setString(1, enteredUsername);
                    adminPs.setString(2, enteredPassword);
                    resultSet = adminPs.executeQuery();

                    if (resultSet.next()) {
                        JOptionPane.showMessageDialog(this, "Login as Admin Successful", "Success",
                                JOptionPane.INFORMATION_MESSAGE);
                        AdminDashboard adminDashboard = new AdminDashboard(selectedCompany);
                        adminDashboard.setVisible(true);
                        dispose();
                    } else {
                        JOptionPane.showMessageDialog(this, "Invalid Credentials for Admin", "Error",
                                JOptionPane.ERROR_MESSAGE);
                    }

                } else if (user.isSelected()) {
                    String userQuery = "SELECT * FROM users WHERE username=? AND password=?";
                    PreparedStatement userPs = con.prepareStatement(userQuery);
                    userPs.setString(1, enteredUsername);
                    userPs.setString(2, enteredPassword);
                    resultSet = userPs.executeQuery();

                    if (resultSet.next()) {
                        JOptionPane.showMessageDialog(this, "Login as User Successful", "Success",
                                JOptionPane.INFORMATION_MESSAGE);
                        UserDashboard userDashboard = new UserDashboard(enteredUsername);
                        userDashboard.setVisible(true);
                        dispose();
                    } else {
                        JOptionPane.showMessageDialog(this, "Invalid Credentials for User", "Error",
                                JOptionPane.ERROR_MESSAGE);
                    }
                }
            } else {
                JOptionPane.showMessageDialog(this, "Company database not found", "Error", JOptionPane.ERROR_MESSAGE);
            }
            centralCon.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

